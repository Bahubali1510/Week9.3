package automationtest.Polymerphism14;

public class Car {

        private String company_name;
        private String model_name;
        private int year;
        double mileage;

        // Getter and Setter methods for company_name
        public String getCompany_name() {
            return company_name;
        }

        public void setCompany_name(String company_name) {
            this.company_name = company_name;
        }

        // Getter and Setter methods for model_name
        public String getModel_name() {
            return model_name;
        }

        public void setModel_name(String model_name) {
            this.model_name = model_name;
        }

        // Getter and Setter methods for year
        public int getYear() {
            return year;
        }

        public void setYear(int year) {
            this.year = year;
        }

        // Getter method for mileage (no setter for mileage)
        public double getMileage() {
            return mileage;
        }
    }


